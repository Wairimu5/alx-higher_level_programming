#!/usr/bin/python3

class Square:
    """
    This is the Square class.

    This class represents a square and has an attribute 'size' to store the size of the square.

    Attributes:
        __size (int): The size of the square.

    Args:
        size (int): The size of the square to be assigned to the '__size' attribute.

    """
    def __init__(self, size):
        self.__size = size
